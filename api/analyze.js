// api/analyze.js
export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Use POST" });
  }

  const expected = process.env.GITHUB_API_KEY;
  const auth = req.headers.authorization || "";
  const token = auth.replace("Bearer ", "");
  if (!expected || token.trim() !== expected.trim()) {
    return res.status(401).json({ error: "Unauthorized" });
  }

  const { text } = req.body || {};
  if (!text) return res.status(400).json({ error: "Missing text" });

  const OPENAI_KEY = process.env.OPENAI_API_KEY;
  if (!OPENAI_KEY)
    return res.status(500).json({ error: "Missing OPENAI_API_KEY in environment" });

  const prompt = `
You are a professional swim coach and analyst.

Analyze the swim workout below. Return two things:
1️⃣ A formatted human-readable breakdown.
2️⃣ A valid JSON object summarizing the totals.

Your JSON MUST follow this schema:
{
  "totalYards": number,
  "durationMinutes": number,
  "sectionYards": {
    "Warmup": number,
    "Kick": number,
    "Drill": number,
    "Main Set": number,
    "Pull": number,
    "Sprint Finisher": number,
    "Cool Down": number
  },
  "strokePercentages": {
    "Freestyle": number,
    "Backstroke": number,
    "Breaststroke": number,
    "Butterfly": number,
    "Drill": number,
    "Kick": number
  }
}

Then output the formatted text.

Workout:
---
${text}
---

Start your response with:
<JSON>
{ ...json... }
</JSON>
Then after that, write the formatted breakdown.
`;

  try {
    const response = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${OPENAI_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "gpt-4o-mini",
        messages: [{ role: "user", content: prompt }],
        temperature: 0.4,
      }),
    });

    const data = await response.json();
    const content = data.choices?.[0]?.message?.content || "";

    // Extract JSON and text parts
    const jsonMatch = content.match(/<JSON>([\s\S]*?)<\/JSON>/);
    let json = {};
    if (jsonMatch) {
      try {
        json = JSON.parse(jsonMatch[1]);
      } catch (err) {
        console.error("JSON parse failed:", err);
      }
    }

    const formattedText = content.replace(/<JSON>[\s\S]*?<\/JSON>/, "").trim();

    res.status(200).json({
      id: crypto.randomUUID(),
      date: new Date().toISOString(),
      formattedText,
      aiSummary: "Generated by OpenAI analysis",
      distanceYards: json.totalYards || 0,
      durationMinutes: json.durationMinutes || 0,
      sectionYards: json.sectionYards || {},
    });
  } catch (err) {
    console.error("AI error:", err);
    res.status(500).json({ error: "AI request failed", details: err.message });
  }
}
